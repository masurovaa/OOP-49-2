# Программа, которая угадывает число, загаданное пользователем

# Объяснение пользователю
print("Загадайте число от 1 до 100. Я попробую его угадать!")
print("Отвечайте 'больше', если ваше число больше, 'меньше', если меньше, или 'угадал', если я угадал.")

# Начальные значения
low = 1
high = 100
attempts = 0
guesses = []

while True:

    guess = (low + high) // 2
    attempts += 1  # Увеличиваем счетчик попыток
    guesses.append(guess)
    print(f"Я думаю, это число {guess}. Что скажете?")


    answer = input("Введите 'больше', 'меньше' или 'угадал': ").strip().lower()

    # Проверяем ответ пользователя
    if answer == "больше":
        low = guess + 1
    elif answer == "меньше":
        high = guess - 1
    elif answer == "угадал":
        print(f"Ура! Я угадал ваше число {guess} за {attempts} попыток!")
        # Записываем результат в файл
        with open("guess_results.txt", "w") as file:
            file.write(f"Загаданное число: {guess}\n")
            file.write(f"Количество попыток: {attempts}\n")
            file.write(f"Список попыток: {guesses}\n")
        break  # Выходим из цикла
    else:
        # Если пользователь ввел что-то не то
        print("Пожалуйста, вводите только 'больше', 'меньше' или 'угадал'.")

